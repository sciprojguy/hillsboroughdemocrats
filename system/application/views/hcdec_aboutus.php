		        The Democratic   Executive Committee (DEC) is the governing body of the Democratic Party in Hillsborough County. Chartered under the Florida Democratic Party (FDP), we are   charged with maintaining and building relationships between constituents in   Hillsborough County and their elected officials on local, state, and national   levels.
<p align="justify">Our membership is   composed of those Democratic individuals who seek active participation in the   decision-making process of the Democratic Party on a County level. Membership is   divided by precincts, with one man and one woman for every one thousand   registered Democrats in that precinct. 
</p>
                <p align="justify">
			Meetings are held once per month., on the third Monday of the month.  
			Dates and locations are posted on the Party calendar located on the <a href="/hcdec/index">home page</a> of this website. 
                </p>
              <p align="justify">Ultimately, our   goal is to create an increased Democratic presence throughout Hillsborough County.   By strengthening this Democratic foundation, we are able to recruit future   candidates, pursue Democratic ideals and initiatives, and most importantly win   elections on County, State, and National levels.&nbsp;&nbsp; <br>
